public class IncommingCalls {
    private int ID;
    private String number;

    public IncommingCalls(int ID, String number) {
        this.ID = ID;
        this.number = number;
    }

    public int getID() {
        return ID;
    }

    public String getNumber()

    {
        return number;
    }

}
